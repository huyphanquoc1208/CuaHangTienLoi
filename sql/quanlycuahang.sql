-- Tạo cơ sở dữ liệu (nếu chưa tồn tại)
BEGIN
    CREATE DATABASE quanlycuahang;
END
GO

-- Sử dụng cơ sở dữ liệu
USE quanlycuahang;
GO

-- Xóa các bảng nếu đã tồn tại
IF OBJECT_ID('invoice_details', 'U') IS NOT NULL DROP TABLE invoice_details;
IF OBJECT_ID('invoices', 'U') IS NOT NULL DROP TABLE invoices;
IF OBJECT_ID('products', 'U') IS NOT NULL DROP TABLE products;
IF OBJECT_ID('employees', 'U') IS NOT NULL DROP TABLE employees;
GO

-- Tạo bảng employees
CREATE TABLE employees (
    employee_id VARCHAR(10) PRIMARY KEY,
    full_name NVARCHAR(100) NOT NULL,
    dob VARCHAR(10),
    phone VARCHAR(15),
    gender NVARCHAR(10),
    position NVARCHAR(50),
    salary FLOAT
);
GO

-- Tạo bảng products
CREATE TABLE products (
    product_id VARCHAR(10) PRIMARY KEY,
    product_name NVARCHAR(100) NOT NULL,
    quantity INT,
    product_type NVARCHAR(50),
    price FLOAT
);
GO

-- Tạo bảng invoices
CREATE TABLE invoices (
    invoice_id VARCHAR(20) PRIMARY KEY,
    invoice_date VARCHAR(10),
    total_amount FLOAT
);
GO

-- Tạo bảng invoice_details
CREATE TABLE invoice_details (
    invoice_id VARCHAR(20),
    product_name NVARCHAR(100),
    quantity INT,
    unit_price FLOAT,
    total_price FLOAT,
    CONSTRAINT FK_invoice_details_invoices FOREIGN KEY (invoice_id) REFERENCES invoices(invoice_id)
);
GO

